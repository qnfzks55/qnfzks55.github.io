오토(Otto, N. A.)
독일의 기계 기술자 (1831~1891). 처음에는 쾰른의 신분이
낮은 상인이었으나, 1861년에 가솔린 기관의 연구를 시작하
여, 1877년에 4사이클식의 오토 기관에 대한 특허를 받았다.
오토 기관은 당시 산업의 원동력이었던 증기 기관 대신에
원동기로서 진출하기 시작하여 현대의 가솔린 기관의 원형
이 되었다.
