가솔린 기관의 작동 원리
가솔린 기관은 공기와 연료의 혼합기 또는 공기를 기관 실린더 연소실
내로 흡입한 다음 혼합기를 압축한 후, 전기적 신호에 의해 강재로 불꽃
을 점화시켜 연소를 일으키는 기관이다. 따라서, 가솔린 기관에는 고전
압의 전기 발생 및 배전을 위한 점화 장치를 필요로 한다. 이기관은 독
일 사람 오토(Otto, N. A.)에 의하여 발명되었으며, 그 이름을 따서 오토
기관이라고도 부른다.
