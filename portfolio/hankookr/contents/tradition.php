⑥ 공동생활과 친교활동을 위해 야간생활 지도교사와 기숙사에서 함께 숙식을 한다.<br>
⑦ 학부모와 지역사회인사의 의견을 적극 수렴하여 열린교육의 장이 되게 한다.<br>
⑧ 운영에 필요한 기본경비는 공동실습소에서 부담한다.
